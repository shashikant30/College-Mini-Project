const Farmer = require("../models/farmer.model.js");
exports.findAll = (req, res) => {
    Farmer.getAll((err, data) => {
      if (err)
        res.status(500).send({
          message:
            err.message || "Some error occurred while retrieving customers."
        });
      else res.send(data);
    });
  };
